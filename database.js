function performSafetyChecks(domain) {
  const checks = {
    hasCommonTLD: checkCommonTLD(domain),
    looksSuspicious: checkSuspiciousPatterns(domain),
    isShortDomain: domain.length < 10,
    hasSpecialChars: /[^a-zA-Z0-9.-]/.test(domain)
  };

  const passedChecks = Object.values(checks).filter(Boolean).length;
  const totalChecks = Object.keys(checks).length;
  const safetyScore = (totalChecks - passedChecks) / totalChecks;

  let message = "‚ö†Ô∏è –°–∞–π—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ –±–∞–∑–µ";
  let riskLevel = "medium";

  if (checks.looksSuspicious) {
    message = "üö® –í–ù–ò–ú–ê–ù–ò–ï: –ü–æ–¥–æ–∑—Ä–∏—Ç–µ–ª—å–Ω—ã–π –¥–æ–º–µ–Ω!";
    riskLevel = "high";
  } else if (!checks.hasCommonTLD) {
    message = "‚ö†Ô∏è –ù–µ—Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –¥–æ–º–µ–Ω –≤–µ—Ä—Ö–Ω–µ–≥–æ —É—Ä–æ–≤–Ω—è";
    riskLevel = "medium";
  } else if (safetyScore > 0.7) {
    message = "‚úÖ –î–æ–º–µ–Ω –≤—ã–≥–ª—è–¥–∏—Ç –Ω–æ—Ä–º–∞–ª—å–Ω–æ";
    riskLevel = "low";
  }

  return {
    message: message,
    details: {
      riskLevel: riskLevel,
      safetyScore: Math.round(safetyScore * 100),
      checks: checks,
      recommendations: getRecommendations(checks)
    }
  };
}

/**
* –ü—Ä–æ–≤–µ—Ä–∏—Ç—å –æ–±—â–∏–π TLD
*/
function checkCommonTLD(domain) {
  const commonTLDs = [
    '.com', '.ru', '.org', '.net', '.edu', '.gov',
    '.io', '.co', '.dev', '.me', '.info', '.biz'
  ];
  return commonTLDs.some(tld => domain.endsWith(tld));
}

