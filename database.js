/**
* Получить информацию о сайте
*/
function getSiteInfo(domain) {
  // Прямое совпадение
  if (SAFE_SITES_PRO[domain]) {
    return SAFE_SITES_PRO[domain];
  }

  // Проверка поддоменов
  const parts = domain.split('.');
  if (parts.length > 2) {
    for (let i = 1; i < parts.length; i++) {
      const parentDomain = parts.slice(i).join('.');
      if (SAFE_SITES_PRO[parentDomain]) {
        return SAFE_SITES_PRO[parentDomain];
      }
    }
  }

  return null;
}

